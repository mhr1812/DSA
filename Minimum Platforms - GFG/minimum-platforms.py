class Solution:    
    #Function to find the minimum number of platforms required at the
    #railway station such that no train waits.
    def minimumPlatform(self,n,arr,dep):
        # code here
        arr.sort(),dep.sort()
        x,y = 1,0
        c =ans= 1
        while x<n and y<n:
            if dep[y]>=arr[x]:
                c+=1
                x+=1
            else:
                c-=1
                y+=1
            ans = max(ans,c)
        return ans
